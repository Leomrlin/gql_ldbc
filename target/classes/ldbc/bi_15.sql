--给定时间范围内创建的论坛可信连接路径
--描述
--根据互相回复消息的交互数量计算每两个人之间的距离
--对于给定的两个人，计算他们之间的最短距离值
--参数：$person1Id bigint = 1100006  $person2Id bigint = 1100007
--$startDate bigint = 1600000000000  $endDate bigint = 1700000000000
--输出：weight double
CREATE FUNCTION bi15 AS 'com.antgroup.gryphon.dsl.udf.ldbc.BIConnectionPathAlgorithm';

