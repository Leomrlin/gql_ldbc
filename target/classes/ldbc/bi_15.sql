USE GRAPH bi;

CREATE TABLE tbl_result (
  weight double
) WITH (
	type='file',
	geaflow.dsl.file.path='${target}'
);

--GQL
INSERT INTO tbl_result
SELECT case when weight > 99999999 then -1.0 else ROUND(weight, 2) end
FROM (
  CALL bi15(1100006, 1100007, 1600000000000, 1700000000000) YIELD (distance)
  RETURN MIN(distance) as weight
)
;